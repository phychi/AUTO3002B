void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	if(aRxBuffer == '\n'){
		LCD_ClearLine(4);
		LCD_DisplayStringLine(4, Rx_buf);
		Rx_Count = 0;
		memset(Rx_buf,'\0',sizeof(Rx_buf));
	}else{
		if(Rx_Count<79){
			Rx_buf[Rx_Count++] = aRxBuffer;
			Rx_buf[Rx_Count] = '\0';
		}
		else{
			memset(Rx_buf,'\0',sizeof(Rx_buf));
			Rx_Count = 0;
		}
	}
	HAL_UART_Receive_IT(&huart1, &aRxBuffer, 1);
	HAL_UART_Receive_IT(&huart1, &aRxBuffer, 1);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	sprintf((char *)txBuf, "%5d", count);
	if(count == 9) count = 0;
	else count++;
	HAL_UART_Transmit(&huart1, txBuf, strlen((char *)txBuf), 1000);
	LCD_ClearLine(2);
	LCD_DisplayStringLine(2, txBuf);
}
